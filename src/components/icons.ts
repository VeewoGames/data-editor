export function readRecentSharedViewIconIds(storage: Storage | null) {
  if (!storage) return [];
  try {
    const parsed = JSON.parse(storage.getItem(sharedViewRecentIconStorageKey) ?? "[]");
    return Array.isArray(parsed)
      ? parsed.filter((value): value is keyof typeof sharedViewIconRegistry => typeof value === "string" && value in sharedViewIconRegistry).slice(0, 12)
      : [];
  } catch {
    return [];
  }
}
